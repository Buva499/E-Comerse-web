package in.smarttech.model;

public class ViewCartDto {
	private int custid;
	private int pno;
	private int qty;
	private int tcost;
	public int getCustid() {
		return custid;
	}
	public void setCustid(int custid) {
		this.custid = custid;
	}
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public int getTcost() {
		return tcost;
	}
	public void setTcost(int tcost) {
		this.tcost = tcost;
	}
	
	
}
